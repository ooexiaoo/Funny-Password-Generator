  function generatePassword() {
    const triggerIndex = document.getElementById('triggerSelect').value;
  
    const options = triggers[triggerIndex].options;
  
    const randomOption = options[Math.floor(Math.random() * options.length)];
  
    document.getElementById('password').value = randomOption;
  }
  
  function copyToClipboard() {
    const passwordText = document.getElementById('password');
    passwordText.select();
    passwordText.setSelectionRange(0, 99999); /* For mobile devices */
  
    document.execCommand('copy');
    alert('Password copied to clipboard!');
  }